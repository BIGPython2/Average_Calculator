# 평균 강도율 계산기

# 도수율이 0일때 평균 강도율 계산할 수 없음을 설정
def 평균_강도율_계산(강도율, 도수율):
    if 도수율 == 0:
        return None

# 평균 강도율 계산식
    평균_강도율 = (강도율 / 도수율) * 1000
    return 평균_강도율

#
def 입력값():
    입력값들 = []
    반복 = True

    # 강도율과 도수율 값 입력받고, 반복 변수가 True인 동안 반복문 계속 실행
    while 반복:
        강도율 = input("강도율을 입력하세요: ")
        도수율 = input("도수율을 입력하세요: ")
        입력값들.append((float(강도율), float(도수율)))

        # 추가 입력할 것인지 묻고 반복하거나 종료
        계속 = input("추가 입력하시겠습니까? (Y/N): ")
        if 계속.lower() != 'Y':
            반복 = False

    return 입력값들

# 함수 호출하여 값 입력받음
def 메인():
    입력값들 = 입력값()

    for 강도율, 도수율 in 입력값들:
        결과 = 평균_강도율_계산(강도율, 도수율)
        if 결과 is not None:
            print("평균 강도율:", 결과)

# 메인 함수 호출
if __name__ == "__main__":
    메인()
